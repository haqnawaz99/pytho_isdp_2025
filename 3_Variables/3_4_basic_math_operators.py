# ==========================================
# 🧮 Python Basic Mathematical Operators
# ==========================================

# These operators work with numbers like int (integers) and float (decimal numbers)

# 1. ➕ Addition (+)
# Used to add two numbers
print("1. Addition:")
print(5 + 3)  # 8

# 2. ➖ Subtraction (-)
# Used to subtract one number from another
print("2. Subtraction:")
print(10 - 4)  # 6

# 3. ✖️ Multiplication (*)
# Used to multiply two numbers
print("3. Multiplication:")
print(6 * 4)  # 24

# 4. ➗ Division (/)
# Used to divide a number (gives decimal result)
print("4. Division:")
print(12 / 4)  # 3.0

# 5. 🧮 Floor Division (//)
# Gives result without decimal (only whole number)
print("5. Floor Division:")
print(13 // 4)  # 3

# 6. 🧠 Modulus (%)
# Gives remainder after division
print("6. Modulus (Remainder):")
print(13 % 4)  # 1

# 7. 🔁 Exponent (**)
# Used to raise one number to the power of another
print("7. Exponent (Power):")
print(2 ** 3)  # 8 → means 2*2*2
