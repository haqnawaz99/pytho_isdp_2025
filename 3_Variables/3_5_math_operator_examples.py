# 1. ➕ Addition (+)
print("1. Addition (+):")
# 📗 Example 1: Adding ages of two Hafiz students
print("Total age of Hafiz students:", 12 + 15)

# 📘 Example 2: Total number of books in two Islamic subjects
print("Total Islamic books:", 40 + 30)

# 🇵🇰 Example 3: Add monthly electricity bills
print("Total bills of two months:", 1200 + 1350)

print()  # Blank line

# 2. ➖ Subtraction (-)
print("2. Subtraction (-):")
# 📗 Example 1: Subtract used copies from total
print("Remaining Copies:", 50 - 18)

# 📘 Example 2: Subtract absent students from total
print("Present students:", 25 - 3)

# 🇵🇰 Example 3: Subtract discount from product price
print("Final price after discount:", 1000 - 150)

print()

# 3. ✖️ Multiplication (*)
print("3. Multiplication (*):")
# 📗 Example 1: Ajza in Quran × number of students
print("Total ajza memorized:", 30 * 5)

# 📘 Example 2: Pages per book × number of books
print("Total pages:", 200 * 4)

# 🇵🇰 Example 3: Price per kg × 5 kg sugar
print("Total cost of sugar:", 170 * 5)

print()

# 4. ➗ Division (/)
print("4. Division (/):")
# 📗 Example 1: Divide zakat money equally
print("Each person receives:", 10000 / 5)

# 📘 Example 2: Divide 12 Hadith books among students
print("Books per student:", 12 / 3)

# 🇵🇰 Example 3: Divide petrol bill among friends
print("Each friend pays:", 1200 / 3)

print()

# 5. 🧮 Floor Division (//)
print("5. Floor Division (//):")
# 📗 Example 1: Qurans placed in 3 rows equally
print("Qurans per row:", 25 // 3)

# 📘 Example 2: Divide chairs in rows (without leftovers)
print("Chairs per row:", 40 // 6)

# 🇵🇰 Example 3: Rupees per child when sharing money
print("Rupees per child:", 1000 // 7)

print()

# 6. 🔁 Modulus (%)
print("6. Modulus (%):")
# 📗 Example 1: Leftover Qurans after equal distribution
print("Leftover Qurans:", 25 % 3)

# 📘 Example 2: Remainder when distributing sweets
print("Leftover sweets:", 40 % 6)

# 🇵🇰 Example 3: Rupees that can't be divided equally
print("Remainder amount:", 1000 % 7)

print()

# 7. 🔼 Exponent (**)
print("7. Exponent (**):")
# 📗 Example 1: 2 times tasbeeh repeated 3 times
print("Total tasbeeh count:", 2 ** 3)

# 📘 Example 2: One ayah memorized and revised 4 times
print("Total repetitions:", 1 ** 4)

# 🇵🇰 Example 3: 3 raised to power 2 (math practice)
