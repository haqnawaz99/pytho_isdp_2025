# Module 3 - Lesson 3.1
# Learning objective: int, float, str, bool and type()

# Integer
age = 17
marks = 95
students = 32
print("Age:", age)
print("Type of age:", type(age))

# Float
price_rupees = 250.50
temperature = 38.5
print("Price:", price_rupees)
print("Type of price:", type(price_rupees))

# String
name = "Fatima"
city = "Lahore"
subject = "Quran"
print("Name:", name)
print("Type of name:", type(name))

# Boolean
is_student = True
fee_paid = False
print("Is student:", is_student)
print("Type of is_student:", type(is_student))

# Pakistan: readable large number
population = 220_000_000
print("Population (approx):", population)

# Mixing types — wrong and right
years = 14
# print("Age: " + years)  # Uncomment to see TypeError
print("Age: " + str(years))
print("Age:", years)

