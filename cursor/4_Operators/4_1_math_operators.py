# Learning objective: + - * / // % **

print("Addition:", 40 + 30)
print("Subtraction (present):", 30 - 4)
print("Multiplication (rupees):", 250 * 6)

print("Division (float):", 12 / 4)
print("Division decimal:", 25 / 3)

print("Floor division:", 25 // 3)
print("Remainder:", 25 % 3)

print("Power 2**3:", 2 ** 3)
print("Square 5**2:", 5 ** 2)

