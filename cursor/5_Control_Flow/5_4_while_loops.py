# Module 5 - Lesson 5.4
# Learning objective: while loops and +=

count = 1
while count <= 5:
    print("Count:", count)
    count += 1

# Count steps
step = 1
while step <= 3:
    print(f"Revision step {step}")
    step += 1

# Study minutes
minutes = 0
while minutes < 30:
    print(f"Studied {minutes} minutes so far")
    minutes += 10

# Simple counter with message
n = 1
while n <= 5:
    print(f"Prayer slot {n} logged")
    n += 1

