# Module 1 - Lesson 1.2
# Learning objective: upper, lower, title, strip, replace, chain methods

# Case
print("lahore".upper())
print("PESHAWAR".lower())
print("assalamu alaikum".title())
print("the holy quran".capitalize())

# Strip whitespace
print("   Fajr Prayer   ".strip())
print("  Samosa stall".lstrip())
print("Karachi port   ".rstrip())

# Replace
print("Peace be upon you".replace("Peace", "Salam"))
print("I go by bus".replace("bus", "metro"))

# Chain
print("  pakistan zindabad  ".strip().title())
print("  ahmed ibn ali  ".strip().title())

