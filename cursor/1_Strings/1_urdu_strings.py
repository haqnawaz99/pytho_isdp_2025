# Learning objective: strings work with Urdu and Arabic text

# Urdu examples - same rules: quotes and + for concatenation
print("السلام" + " علیکم")
print("میرا" + " " + "نام" + " " + "احمد" + " " + "ہے")

# Arabic example
print("بسم" + " " + "الله" + " " + "الرحمن" + "الرحيم")

# Mix English label + Urdu value
label = "Subject: "
subject_urdu = "علوم القرآن"
print(label + subject_urdu)

# English + Urdu sentence
print("We are learning" + " " + "Python")

# len() works on Urdu text too
print(len("لاہور"))

