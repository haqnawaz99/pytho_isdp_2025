# Example 1: Saying Salam
print("السلام" + " علیکم")  # Output: السلام علیکم

# Example 2: Basic greeting
print("خوش آمدید" + " " + "میرے پیارے طالبعلموں")  # Output: خوش آمدید میرے پیارے طالبعلموں

# Example 3: Joining name parts
print("مولانا" + " " + "عبدالرحمن")  # Output: مولانا عبدالرحمن

# Example 4: Simple sentence
print("ہم" + " " + "Python" + " " + "سیکھ رہے ہیں")  # Output: ہم Python سیکھ رہے ہیں

# Example 5: Adding punctuation
print("سبق" + " " + "شروع" + " " + "کرتے ہیں" + "!")  # Output: سبق شروع کرتے ہیں!

# Example 6: Arabic + Urdu sentence
print("بِسْمِ" + " " + "اللّٰہِ" + " " + "الرَّحْمٰنِ" + " " + "الرَّحِیْمِ")  # Output: بِسْمِ اللّٰہِ الرَّحْمٰنِ الرَّحِیْمِ

# Example 7: Motivational sentence
print("محنت" + " " + "کامیابی" + " " + "کی" + " " + "کنجی" + " " + "ہے")  # Output: محنت کامیابی کی کنجی ہے

# Example 8: Simple English sentence
print("This" + " " + "is" + " " + "my" + " " + "first" + " " + "program.")  # Output: This is my first program.

# Example 9: Full verse-style print
print("علم" + " " + "حاصل" + " " + "کرنا" + " " + "ہر" + " " + "مسلمان" + " " + "پر" + " " + "فرض" + " " + "ہے۔")  
# Output: علم حاصل کرنا ہر مسلمان پر فرض ہے۔

# Example 10: Quranic command (without translation)
print("اقْرَأْ" + " " + "بِاسْمِ" + " " + "رَبِّكَ" + " " + "ٱلَّذِى" + " " + "خَلَقَ")  
# Output: اقْرَأْ بِاسْمِ رَبِّكَ ٱلَّذِى خَلَقَ
