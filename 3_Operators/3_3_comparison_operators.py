# ==================================================
# 🟰 Operators: Comparison Operators
# ==================================================
# 🎯 Learning Objective: Learn ==, !=, >, <, >=, <=
# 📚 Madrasa Context: Age, study hours, prayer time, exam score

print("🟰 Welcome to Comparison Operators")
print("=" * 50)

# ----------------------------------
# 1. Equal to (==)
# ----------------------------------
print("\n1. Equal to (==):")
print("'Fajr' == 'Fajr' ->", "Fajr" == "Fajr")
print("'Maghrib' == 'Isha' ->", "Maghrib" == "Isha")

# ----------------------------------
# 2. Not equal to (!=)
# ----------------------------------
print("\n2. Not equal to (!=):")
print("'Ramadan' != 'Shawwal' ->", "Ramadan" != "Shawwal")
print("'Friday' != 'Friday' ->", "Friday" != "Friday")

# ----------------------------------
# 3. Greater than (>)
# ----------------------------------
print("\n3. Greater than (>):")
print("Study hours 4 > 2 ->", 4 > 2)
print("Age 8 > 10 ->", 8 > 10)

# ----------------------------------
# 4. Less than (<)
# ----------------------------------
print("\n4. Less than (<):")
print("Minutes until prayer 5 < 10 ->", 5 < 10)
print("Marks 65 < 50 ->", 65 < 50)

# ----------------------------------
# 5. Greater than or equal to (>=)
# ----------------------------------
print("\n5. Greater than or equal to (>=):")
print("Exam score 70 >= 70 ->", 70 >= 70)
print("Age 9 >= 10 ->", 9 >= 10)

# ----------------------------------
# 6. Less than or equal to (<=)
# ----------------------------------
print("\n6. Less than or equal to (<=):")
print("Quran level 3 <= 5 ->", 3 <= 5)
print("Age 12 <= 6 ->", 12 <= 6)

