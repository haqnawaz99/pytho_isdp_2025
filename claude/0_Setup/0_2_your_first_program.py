# ------------------------------------------------
# The print() function
# ------------------------------------------------
# print() is the most basic Python command.
# It shows text or numbers on the screen.
# Whatever you put inside the brackets gets printed.

print("Hello, World!")
print("Assalamu Alaikum!")
print("I am a Python programmer now.")

# ------------------------------------------------
# Printing numbers
# ------------------------------------------------
# Numbers do NOT need quotation marks

print(10)
print(100)
print(2025)

# ------------------------------------------------
# Printing calculations
# ------------------------------------------------
# Python can calculate and print the answer

print(5 + 3)        # Addition
print(10 - 4)       # Subtraction
print(3 * 4)        # Multiplication
print(10 / 2)       # Division

# ------------------------------------------------
# Printing text AND numbers together
# ------------------------------------------------
# Use a comma to combine text and numbers in print()

print("My age is", 14)
print("Pakistan has", 220000000, "people")
print("There are", 5, "prayers in a day")

# ------------------------------------------------
# Multiple print statements
# ------------------------------------------------
# Each print() starts on a new line

print("Fajr")
print("Dhuhr")
print("Asr")
print("Maghrib")
print("Isha")

# ------------------------------------------------
# Empty print() adds a blank line
# ------------------------------------------------
print("First section")
print()
print("Second section after blank line")

