# ------------------------------------------------
# What is a comment?
# ------------------------------------------------
# A comment is a note for the programmer.
# Python IGNORES comments completely.
# Comments start with the # symbol.
# Use comments to explain your code.

# This is a comment. Python will not run this line.
print("Python runs this line.")   # This comment is after code - also ignored

# ------------------------------------------------
# Why write comments?
# ------------------------------------------------
# 1. To explain what your code does
# 2. To remind yourself later
# 3. To help other people understand your code
# 4. To temporarily turn off a line of code

# ------------------------------------------------
# Examples of useful comments
# ------------------------------------------------

# Print the Islamic greeting
print("Assalamu Alaikum wa Rahmatullahi wa Barakatuh")

# Show the number of Surahs in the Quran
print("The Quran has", 114, "Surahs")

# Show Pakistan's independence year
print("Pakistan was created in", 1947)

# ------------------------------------------------
# Turning off code with a comment
# ------------------------------------------------
# Sometimes we want to stop a line from running.
# Just put # in front of it.

print("This line runs.")
# print("This line is turned off - Python ignores it.")
print("This line runs too.")

# ------------------------------------------------
# Good commenting habit
# ------------------------------------------------
# Write comments that explain WHY, not just WHAT.

# Bad comment (obvious - no need to say this):
# print the word hello
print("Hello")

# Good comment (explains something non-obvious):
# Greet the student by name before starting the lesson
print("Welcome, Ahmed! Let us learn Python today.")

# ------------------------------------------------
# Summary
# ------------------------------------------------
# # starts a comment
# Python ignores all comments
# Comments are for humans, not computers
# Good programmers write helpful comments

print()
print("You now know about comments. Well done!")
