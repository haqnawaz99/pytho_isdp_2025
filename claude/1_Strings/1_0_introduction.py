# ------------------------------------------------
# What is a String?
# ------------------------------------------------
# A string is any text inside quotation marks.
# It can be letters, words, sentences, or symbols.
# Python treats everything inside " " as text.

# These are all strings:
print("Assalamu Alaikum")
print("Pakistan")
print("123")           # Even numbers become text when inside quotes
print("!")
print("Surah Al-Fatiha has 7 verses")

# ------------------------------------------------
# Single quotes or double quotes - both work
# ------------------------------------------------
print("This uses double quotes")
print('This uses single quotes')

# Both give the same result.
# Pick one style and stay consistent.

# ------------------------------------------------
# What is NOT a string
# ------------------------------------------------
# Numbers WITHOUT quotes are actual numbers (not strings)
# Python treats them differently

print(786)             # This is a number
print("786")           # This is a string (text)

# We will learn the difference more in Module 3 (Data Types)

# ------------------------------------------------
# Strings can be long or short
# ------------------------------------------------
print("Hi")
print("The Holy Quran was revealed to Prophet Muhammad (PBUH) over 23 years")
print("Lahore")
print("May Allah bless you and your family. Ameen.")

# ------------------------------------------------
# Summary
# ------------------------------------------------
# - Text inside " " or ' ' is called a string
# - print() shows a string on the screen
# - Numbers inside quotes are also strings

print()
print("You now understand what a string is. Ma sha Allah!")
