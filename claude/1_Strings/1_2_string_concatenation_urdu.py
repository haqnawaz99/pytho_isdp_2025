# ================================================
# Module 1 - Lesson 2: Concatenation with Urdu & Arabic
# ================================================
# Learn: Python works with Urdu and Arabic text too
# ================================================

# Python supports all languages including Urdu and Arabic.
# The same + operator works for joining Urdu strings.

# ------------------------------------------------
# Urdu Examples
# ------------------------------------------------

print("السلام" + " علیکم")
print("خوش آمدید " + "میرے پیارے طالبعلموں")
print("مولانا " + "عبدالرحمن")
print("ہم " + "Python " + "سیکھ رہے ہیں")
print("سبق شروع کرتے ہیں" + "!")
print("محنت " + "کامیابی کی کنجی ہے")
print("علم حاصل کرنا " + "ہر مسلمان پر فرض ہے۔")

# ------------------------------------------------
# Arabic Examples
# ------------------------------------------------

print("بِسْمِ اللّٰہِ " + "الرَّحْمٰنِ الرَّحِیْمِ")
print("اقْرَأْ بِاسْمِ رَبِّكَ " + "الَّذِي خَلَقَ")

# ------------------------------------------------
# Mixing English and Urdu
# ------------------------------------------------

print("ہم " + "Python " + "سیکھ رہے ہیں")         # Urdu + English
print("My name is " + "احمد علی")                   # English + Urdu
print("Subject: " + "علوم القرآن")                  # English label + Urdu value

# ------------------------------------------------
# Note for Students
# ------------------------------------------------
# Urdu text goes right to left but Python handles it.
# In the terminal it may look slightly different.
# The logic is the same as English strings.

print()
print("Excellent! Python speaks Urdu and Arabic too. Alhamdulillah!")
