# Example 1: Basic string variable
greeting = "Assalamu Alaikum"
print(greeting)

# Example 2: Name variable
name = "Ahmed"
print("My name is " + name)

# Example 3: Combining two string variables
first_name = "Hafiz"
last_name = "Ali"
print(first_name + " " + last_name)

# Example 4: Add a sentence with a string variable
city = "Lahore"
print("I live in " + city)

# Example 5: Use input to set a string variable
subject = input("Enter your favorite subject: ")
print("You like " + subject)
