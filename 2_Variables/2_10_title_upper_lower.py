# In Python, we can change the style of words and names using simple functions.

# ----------------------------------
# ðŸ”¹ 1. title() â†’ Title Case
# ----------------------------------
# Changes the first letter of every word to a capital letter.

student_name = "muhammad musa"
print(student_name.title())

# ----------------------------------
# ðŸ”¹ 2. upper() â†’ Uppercase
# ----------------------------------
# Changes all letters into CAPITAL letters.

student_name = "muhammad abdullah"
print(student_name.upper())

# ----------------------------------
# ðŸ”¹ 3. lower() â†’ Lowercase
# ----------------------------------
# Changes all letters into small letters.

institution_name = "Jamia Ashrafia"
print(institution_name.lower())

# ----------------------------------
# ðŸ”¹ Another Example for Practice
# ----------------------------------
# You can also use these methods with any city name, book title, or subject.

city_name = "lahore"
print(city_name.title())
print(city_name.upper())
print(city_name.lower())

