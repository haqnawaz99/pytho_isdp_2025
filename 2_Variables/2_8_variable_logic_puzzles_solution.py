# variable_logic_puzzles_solution.py

# ✅ Puzzle 1: Swap two values
a = "pen"
b = "book"
temp = a
a = b
b = temp
print("a:", a)
print("b:", b)

# ✅ Puzzle 2: Create full name
first_name = "Hafiz"
last_name = "Usman"
full_name = first_name + " " + last_name
print("Full Name:", full_name)

# ✅ Puzzle 3: Chain swapping
box1 = "dates"
box2 = "apples"
box3 = "bananas"
temp = box1
box1 = box3
box3 = box2
box2 = temp
print("box1:", box1)
print("box2:", box2)
print("box3:", box3)

# ✅ Puzzle 4: Secret code
word1 = "peace"
word2 = "love"
secret = word2 + word1
print("Secret Code:", secret)

# ✅ Puzzle 5: Repeat and combine
item = "Quran"
repeat_item = item + " " + item + " " + item
print("Repeated:", repeat_item)
