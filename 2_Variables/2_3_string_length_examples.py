# string_length_examples.py

# Example 1: Length of a simple word
print(len("Quran"))

# Example 2: Length of a full name
print(len("Maulana Tariq Jameel"))

# Example 3: Using len() with a variable
message = "Assalamu Alaikum"
print(len(message))

# Example 4: Ask the user for their name, then show its length
name = input("Enter your name: ")
print("The number of characters in your name is: " + str(len(name)))

# Example 5: Ask the user for a Surah name, then show length
surah = input("Enter your favorite Surah: ")
print("Length of Surah name is: " + str(len(surah)))
