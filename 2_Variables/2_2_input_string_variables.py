# Example 1: Ask name and store it in a variable
name = input("What is your name? ")
print("Welcome " + name)

# Example 2: Ask favorite book
book = input("Enter your favorite Islamic book: ")
print("You like the book " + book)

# Example 3: Ask madrasa name
